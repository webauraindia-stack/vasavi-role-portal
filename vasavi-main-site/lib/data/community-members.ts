export function lookupMemberProfile(
  query: string
): CommunityMemberProfile | null {
  const normalized = query.trim().toUpperCase().replace(/\s/g, "");
  if (!normalized) return null;

  return (
    MOCK_MEMBER_PROFILES.find(
      (p) =>
        p.memberId.toUpperCase() === normalized ||
        p.displayId.toUpperCase() === normalized ||
        p.email.toLowerCase() === query.trim().toLowerCase()
    ) ?? null
  );
}

export function getMemberCategoryLabel(category: MemberCategory): string {
  const labels: Record<MemberCategory, string> = {
    vci_life_member: "VCI Life Member",
    vksp_member: "VKSP Member",
    kcgf_donor: "KCGF Donor",
    service_volunteer: "Seva Volunteer",
    sponsorship_patron: "Sponsorship Patron",
    senior_member: "Senior Member",
    community_guest: "Guest",
  };
  return labels[category];
}
